    /**
     * 设置16灰阶的LUT（LUT波形）
     * @param grayScale
     * @throws IOException
     */
    public void setSet16GrayLut(int grayScale) throws IOException {

        for4in2Demo.sendCommand(0x20);
        for (int data : Epaper4in2_16GrayScale.EPD_4IN2_PARTIAL_LUT_VCOM1) {
            for4in2Demo.sendData(data);
        }
        for4in2Demo.sendCommand(0x21);
        for (int data : Epaper4in2_16GrayScale.EPD_4IN2_PARTIAL_LUT_WW1) {
            for4in2Demo.sendData(data);
        }
        for4in2Demo.sendCommand(0x22);
        for (int data : Epaper4in2_16GrayScale.EPD_4IN2_PARTIAL_LUT_BW1) {
            for4in2Demo.sendData(data);
        }
        for4in2Demo.sendCommand(0x23);
        for (int data : Epaper4in2_16GrayScale.EPD_4IN2_16Gray_LUT_WB[grayScale]) {
            for4in2Demo.sendData(data);
        }
        for4in2Demo.sendCommand(0x24);
        for (int data : Epaper4in2_16GrayScale.EPD_4IN2_PARTIAL_LUT_BB1) {
            for4in2Demo.sendData(data);
        }
    }

