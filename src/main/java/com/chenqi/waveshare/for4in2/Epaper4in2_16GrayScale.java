    private For4in2Demo for4in2Demo = For4in2Demo.getInstance();

    /**
     * 打印灰度阶梯
     *
     * @param grayIndex
     * @throws IOException
     * @throws InterruptedException
     */
    public void display16GrayLadder(int grayIndex) throws IOException, InterruptedException {
        System.out.println("start to partialDisplay ======");
        int width = 400;
        int height = 300;

        setPartialSetLut();
        for4in2Demo.sendCommand(0x91);

        for4in2Demo.sendCommand(0x90);
        for4in2Demo.sendData(0); //x-start
        for4in2Demo.sendData(0);
        for4in2Demo.sendData(width / 256); //x-end
        for4in2Demo.sendData((width % 256) - 1);

        for4in2Demo.sendData(0); //y-start
        for4in2Demo.sendData(0);
        for4in2Demo.sendData(height / 256);//y-end
        for4in2Demo.sendData((height % 256) - 1);

        for4in2Demo.sendData(28);

        for4in2Demo.sendCommand(0x10);
        for (int i = 0; i < 300 * 400 / 8; i++) {
            for4in2Demo.sendData(0xFF);
        }

        for4in2Demo.sendCommand(0x13);
        for (int i = 0; i < 300 * 400 / 8; i++) {
            int index = (i % 50) / 3;
            if (index > 15) index = 15;
            if (index > grayIndex) {
                for4in2Demo.sendData(0);
            } else {
                for4in2Demo.sendData(0xFF);
            }
        }
        for4in2Demo.sendCommand(0x12);
        Thread.sleep(200);
        for4in2Demo.readBusy();

    }

    public void setPartialSetLut() throws IOException {

        for4in2Demo.sendCommand(0x20);
        for (int data : Epaper4in2_16GrayScale.EPD_4IN2_PARTIAL_LUT_VCOM1) {
            for4in2Demo.sendData(data);
        }
        for4in2Demo.sendCommand(0x21);
        for (int data : Epaper4in2_16GrayScale.EPD_4IN2_PARTIAL_LUT_WW1) {
            for4in2Demo.sendData(data);
        }
        for4in2Demo.sendCommand(0x22);
        for (int data : Epaper4in2_16GrayScale.EPD_4IN2_PARTIAL_LUT_BW1) {
            for4in2Demo.sendData(data);
        }
        for4in2Demo.sendCommand(0x23);
        for (int data : Epaper4in2_16GrayScale.EPD_4IN2_PARTIAL_LUT_WB1) {
            for4in2Demo.sendData(data);
        }
        for4in2Demo.sendCommand(0x24);
        for (int data : Epaper4in2_16GrayScale.EPD_4IN2_PARTIAL_LUT_BB1) {
            for4in2Demo.sendData(data);
        }
    }


